template <int W, int H, char living = 'O'>
struct InitialState {
    char state[W * H + 1];

    constexpr int operator()(int i, int j) const {
        return i < 0 || i >= W || j < 0 || j >= H
                 ? 0
                 : int(state[i + j * W] == living);
    }
};

